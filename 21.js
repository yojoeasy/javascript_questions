// What is JSON?

// JSON stands for JavaScript Object Notation. It’s a lightweight data format used to store and exchange data between systems (most commonly between a server and a web/app client).

// Think of JSON as a clean, text-based way to represent data that both humans and computers can easily read

// Why JSON is popular
// Easy to read & write
// Easy for machines to parse & generate
// Language-independent (works with Java, Python, PHP, C#, etc.)
// Very common in APIs and web services

// {
//   "name": "Yogesh",
//   "age": 30,
//   "isInvestor": true,
//   "skills": ["stocks", "mutual funds", "analysis"]
// }

// JSON.parse() converts JSON string → JavaScript object
// JSON.stringify() converts JavaScript object → JSON string


// Difference between JSON and JavaScript Object
// Aspect                           JSON                                       JavaScript Object
// Full form                        JavaScript Object Notation                 —
// Type                             Text / String format                       In-memory data structure
// Purpose                          Data storage & transfer                    Data processing & logic
// Language                         Language-independent                       JavaScript only
// Keys                             Must be in double quotes                   Quotes optional
// Values                           Limited data types only                    Can include functions, dates, etc.
// Comments                         ❌ Not allowed                            ✅ Allowed
// Trailing commas                  ❌ Not allowed                            ✅ Allowed  